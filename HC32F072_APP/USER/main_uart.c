/**\function 	main()
 * \return 		uint32_t return value, if needed
 * \note 			Uart中断收发数据 返回该字节的取反值
 ******************************************************************************/
int32_t main(void)
{    
		//系统初始化
		System_Init();
		
    while(1)
    {
				SendToMPU(); //发送指令到显示屏
				
				while(1)
				{		
						Wdt_Feed(); //喂狗
						ParseFromMPU(); //解析显示屏回传数据
				}
    }
}

